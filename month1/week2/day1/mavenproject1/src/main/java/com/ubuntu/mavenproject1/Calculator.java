public class Calculator {
    
    // Method Overloading
    
    int add(int a, int b)
    {
        return a + b;
    }
    
    double add(double a, double b)
    {
        return a + b;
    }
    
    int add (int a, int b, int c)
    {
        return a + b + c;
    }
}
